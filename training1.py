def distance(a, b):
    "this function works out the average of two number a and b"
    return abs(a-b)

def geometric_mean(a, b):
    "this function works out geometric mean of a and b"
    return (a * b) ** 0.5

def pyramid_volume(A, h):
    "this function works out the volume of a pyramid with area A and height h"
    return (A * h)/3.0





